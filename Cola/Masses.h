#include "FourVector/FourVector.h"

/** @name Masses and Particles

    The files Masses.h and Masses.cc define the particle masses. To
    identify the particles the class Particle extends the FourVector class.
*/
//@{
/// Particle with ID, Mass, Width and FourVector
class Particle : public FourVector {
private:
  int id;
  static int lastid;
  static Particle * first;
  Particle *next;
protected:
  const char *name;
  double mass;
  double width;
  int    charge;
  FourVector polarization;
public:
  /// Creates a new particle, the id is incremented automatically
  Particle(const FourVector & FV = FourVector(), 
	   const FourVector & pol = FourVector());
  /// Creates a particle, the id is incremented automatically
  Particle(const char *name, int charge, double mass, double width=0);
  /// Compares two particles. Only the ID is compared, not the FourVector!
  int operator==(const class Particle) const;
  /// Copy the FourVector to the particles FourVector
  // Whith out this operator the class use the constructor and all the 
  // other information of the particle will be lost
  Particle operator=(FourVector);
  /// This is usually not needed
  int getID() const { return id; }
  /// find Particle in chain
  static Particle *find(const char *name);
  /// get the parameters
  const char *getName() const { return name; }
  /// get the parameters
  double getWidth(void) const { return width; }
  /// get the parameters
  double getMass(void) const { return mass; }
  /// get the parameters
  int getCharge(void) const { return charge; }
  /// get the polarization or 4-Spin of the particle
  FourVector getPolarization(void) const {return polarization;}
  /// get the Spin of the particle
  Momentum getSpin(void) const;
  /// Set the momentum of the Particle. Mass is already known.
  void setMomentum(double px, double py, double pz);
  /// Set the Spin of the Particle
  Particle setSpin(const Momentum &s);
  /// Makes a Lorentz transformation to transport the particle to the RS where the reference FourVector is at rest
  Particle Lorentz(const FourVector& reference);
  /// Rotate the particle into the frame where the Z axis will point to the given direction
  Particle rotate(const Momentum& direction) const ;
  /// Rotate the particle into the frame where the given direction points to the old Z axis
  Particle rotateTo(const Momentum& direction) const ;
  /// IOStream output operator
  friend std::ostream& operator<<(std::ostream&, Particle);
};
//@}

